        <h2>Historia</h2>
        <h4 style="text-align: justify;">
          En el año 2000 surge la idea de realizar un ciclo de conferencias enfocadas al ámbito informático, a lo cual año con año los alumnos de Ingeniería en Sistemas Computacionales del IESCH coordinan y ejecutan, con el fin de dar a conocer a alumnos y público en general las nuevas innovaciones tecnológicas tanto nacionales como mundiales a través de la participación de profesores, doctores e investigadores, lo que permite ser un evento de análisis, discusión y proposición de nuevas expectativas de conocimientos en beneficio de todos los asistentes.
        </h4>
        <h2>Objetivo</h2>
        <h4 style="text-align: justify;">
          Dar a conocer a la comunidad Chiapaneca en general las tendencias tecnológicas nacionales y mundiales para fortalecer o incrementar su conocimiento en los temas afines, al mismo tiempo pretendiendo el surgimiento de nuevas ideas en la generación de proyectos pertinentes.
